package lk.sliit.sems_app;

public class Weeks {
    public double day1;
    public double day2;
    public double day3;
    public double day4;
    public double day5;
    public double day6;
    public double day7;

    public Weeks() {
    }

    public Weeks(double day1, double day2, double day3, double day4, double day5, double day6, double day7) {
        this.day1 = day1;
        this.day2 = day2;
        this.day3 = day3;
        this.day4 = day4;
        this.day5 = day5;
        this.day6 = day6;
        this.day7 = day7;
    }

    public double getDay1() {
        return day1;
    }

    public double getDay2() {
        return day2;
    }

    public double getDay3() {
        return day3;
    }

    public double getDay4() {
        return day4;
    }

    public double getDay5() {
        return day5;
    }

    public double getDay6() {
        return day6;
    }

    public double getDay7() {
        return day7;
    }
}
