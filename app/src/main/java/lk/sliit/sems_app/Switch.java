package lk.sliit.sems_app;

public class Switch {
    public int sub1;
    public int sub2;
    public int sub3;

    public Switch(){}

    public Switch(int sub1, int sub2, int sub3) {
        this.sub1 = sub1;
        this.sub2 = sub2;
        this.sub3 = sub3;
    }

    public int getSub1() {
        return sub1;
    }

    public int getSub2() {
        return sub2;
    }

    public int getSub3() {
        return sub3;
    }
}
